export function buildTraceabilityMatrixMock(projectId: string): TraceabilityMatrixData {
  const base = JSON.parse(JSON.stringify(traceabilityMatrixMock)) as TraceabilityMatrixData;
  base.project.id = projectId;
  base.filters.projectId = projectId;

  const sip = "/projects/sip-001";
  const rewrite = (value: string) => value.replaceAll(sip, `/projects/${projectId}`);

  base.phaseArtifactLinks = base.phaseArtifactLinks.map((row) => ({ ...row, href: rewrite(row.href) }));
  base.requirementDesignLinks = base.requirementDesignLinks.map((row) => ({
    ...row,
    href: rewrite(row.href),
  }));
  base.requirementTestLinks = base.requirementTestLinks.map((row) => ({ ...row, href: rewrite(row.href) }));
  base.gateEvidenceLinks = base.gateEvidenceLinks.map((row) => ({ ...row, href: rewrite(row.href) }));
  base.artifactGateLinks = base.artifactGateLinks.map((row) => ({
    ...row,
    href: rewrite(row.href),
    detailHref: rewrite(row.detailHref),
  }));
  base.evidenceApprovalLinks = base.evidenceApprovalLinks.map((row) => ({
    ...row,
    href: rewrite(row.href),
    detailHref: rewrite(row.detailHref),
  }));
  base.traceabilityGaps = base.traceabilityGaps.map((row) => ({ ...row, href: rewrite(row.href) }));
  base.coverageSummary.reportHref = rewrite(base.coverageSummary.reportHref);
  base.actionState.href = rewrite(base.actionState.href);

  return base;
}
